def is_datapackage_cache_current(cache_key: str) -> bool:
    return _load_datapackage_from_cache(cache_key) is not None


def is_world_settings_cache_current(cache_key: str) -> bool:
    return _load_world_settings_cache(cache_key) is not None


def _build_world_settings_cache_data() -> Dict[str, str]:
    from worlds.AutoWorld import AutoWorldRegister
    settings_cache: Dict[str, str] = {}
    for world in AutoWorldRegister.world_types.values():
        annotation = world.__annotations__.get("settings", None)
        if annotation is None or annotation == "ClassVar[Optional['Group']]":
            continue
        settings_cache[world.settings_key] = f"{world.__module__}.{world.__name__}"
    return settings_cache


def calculate_world_cache_key() -> str:
    from worlds import world_sources
    return _calculate_datapackage_cache_key(world_sources)


def load_world_settings_cache(cache_key: str | None = None) -> Dict[str, str] | None:
    from worlds import world_sources
    cache_key = cache_key or _calculate_datapackage_cache_key(world_sources)
    return _load_world_settings_cache(cache_key)


def rebuild_world_settings_cache(cache_key: str | None = None) -> None:
    import worlds
    if not worlds._worlds_loaded:
        return
    cache_key = cache_key or _calculate_datapackage_cache_key(worlds.world_sources)
    _write_world_settings_cache(cache_key, _build_world_settings_cache_data())


def is_launcher_cache_current(cache_key: str) -> bool:
    try:
        from worlds import LauncherComponents
    except Exception:
        return False
    return LauncherComponents.is_launcher_cache_current(cache_key)


def are_world_caches_current(cache_key: str | None = None) -> bool:
    from worlds import world_sources
    cache_key = cache_key or _calculate_datapackage_cache_key(world_sources)
    return (
        is_datapackage_cache_current(cache_key)
        and is_launcher_cache_current(cache_key)
        and is_world_settings_cache_current(cache_key)
    )


def rebuild_world_caches() -> None:
    """Rebuild cache files from current in-memory world state.

    Unlike ensure_worlds_loaded(), this bypasses the _worlds_loaded guard so it can be called
    after hot-loading a new world (e.g. after install_apworld) to keep cache files current
    for subsequent processes without requiring a full reload.
    Does nothing if worlds have not been loaded yet.
    """
    import worlds
    if not worlds._worlds_loaded:
        return
    cache_key = _calculate_datapackage_cache_key(worlds.world_sources)
    worlds._build_network_data_packages()
    _write_datapackage_cache(cache_key, worlds.network_data_package["games"])
    _write_world_settings_cache(cache_key, _build_world_settings_cache_data())
    try:
        from worlds import LauncherComponents
        LauncherComponents.write_launcher_cache(cache_key)
    except Exception as exc:
        logging.warning(f"Failed to write launcher cache during rebuild: {exc}")
