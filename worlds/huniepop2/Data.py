def randomise_stuff(random, options) -> dict:
    outdict = {}

    for girl in girl_list:
        gdict = {}

        # TOKEN RANDO
        if options.randomise_girl_token.value:
            tmp = random.sample(token_list, 2)
            gdict["favtoken"] = tmp[0]
            gdict["distoken"] = tmp[1]
        else:
            gdict["favtoken"] = default_token[girl][0]
            gdict["distoken"] = default_token[girl][1]

        # SHOE RANDO
        if options.randomise_girl_gifts.value:
            tmp = random.sample(shoe_list, 4)
            gdict["shoe1"] = tmp[0]
            gdict["shoe2"] = tmp[1]
            gdict["shoe3"] = tmp[2]
            gdict["shoe4"] = tmp[3]
        else:
            gdict["shoe1"] = default_shoes[girl][0]
            gdict["shoe2"] = default_shoes[girl][1]
            gdict["shoe3"] = default_shoes[girl][2]
            gdict["shoe4"] = default_shoes[girl][3]

        # UNIQUE RANDO
        if options.randomise_girl_gifts.value:
            tmp = random.sample(unique_list, 4)
            gdict["unique1"] = tmp[0]
            gdict["unique2"] = tmp[1]
            gdict["unique3"] = tmp[2]
            gdict["unique4"] = tmp[3]
        else:
            gdict["unique1"] = default_unique[girl][0]
            gdict["unique2"] = default_unique[girl][1]
            gdict["unique3"] = default_unique[girl][2]
            gdict["unique4"] = default_unique[girl][3]

        # BAGGAGE RANDO
        if options.randomise_girl_baggage.value:
            tmp = random.sample(baggage_list, 3)
            gdict["baggage1"] = tmp[0]
            gdict["baggage2"] = tmp[1]
            gdict["baggage3"] = tmp[2]
        else:
            gdict["baggage1"] = default_baggage[girl][0]
            gdict["baggage2"] = default_baggage[girl][1]
            gdict["baggage3"] = default_baggage[girl][2]

        outdict[girl] = gdict

    return outdict
